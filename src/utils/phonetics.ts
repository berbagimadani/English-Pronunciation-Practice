// Function to convert text to phonetic transcription
export const textToPhonetic = (text: string): string => {
  // Clean the text and split into words
  const words = text
    .toLowerCase()
    .replace(/[^\w\s]/g, '') // Remove punctuation
    .split(/\s+/)
    .filter(word => word.length > 0)

  // Convert each word to phonetic or keep original if not found
  const phoneticWords = words.map(word => {
    return phoneticDictionary[word] || word
  })

  return phoneticWords.join(' ')
}

// Function to get phonetic for a single word
export const getWordPhonetic = (word: string): string => {
  const cleanWord = word.toLowerCase().replace(/[^\w]/g, '')
  return phoneticDictionary[cleanWord] || word
}

// Function to check if a word has phonetic transcription
export const hasPhonetic = (word: string): boolean => {
  const cleanWord = word.toLowerCase().replace(/[^\w]/g, '')
  return cleanWord in phoneticDictionary
}